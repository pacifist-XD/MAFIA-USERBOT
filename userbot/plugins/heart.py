import asyncio

from uniborg.util import admin_cmd


@borg.on(admin_cmd(pattern="heart", outgoing=True))
async def _(event):
    await event.edit("❤️")
    await asyncio.sleep(2)
    await event.edit("🧡")
    await asyncio.sleep(2)
    await event.edit("💛")
    await asyncio.sleep(2)
    await event.edit("💚")
    await asyncio.sleep(2)
    await event.edit("💙")
    await asyncio.sleep(2)
    await event.edit("💜")
    await asyncio.sleep(2)
    await event.edit("🖤")
    await asyncio.sleep(2)
    await event.edit("🤍")
    await asyncio.sleep(2)
    await event.edit("🤎")
    await asyncio.sleep(2)
    await event.edit("💝")
    await asyncio.sleep(2)
    await event.edit("💘")
